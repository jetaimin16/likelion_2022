// if 조건문
if (10 < 20){
    console.log("10은 20보다 작습니다.");
}

if (10 > 20){
    console.log("20은 10보다 작습니다.");
} else {
    console.log("20은 10보다 큽니다.");
}

if (30 < 10) {
    console.log("30은 10보다 작습니다.");
} else if (20 < 20) {
    console.log("30은 20보다 작습니다.");
} else if (30 < 30) {
    console.log("30은 30보다 작습니다.");
} else {
    console.log("모든 if문의 조건이 거짓입니다.");
}